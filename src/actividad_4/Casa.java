
package actividad_4;


public class Casa {
    private String zona;
    private int piso;

    public Casa(String zona, int piso) {
        this.zona = zona;
        this.piso = piso;
    }

    public String getZona() {
        return zona;
    }

    public void setZona(String zona) {
        this.zona = zona;
    }

    public int getPiso() {
        return piso;
    }

    public void setPiso(int piso) {
        this.piso = piso;
    }
    
    
}
